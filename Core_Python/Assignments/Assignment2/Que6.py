## Program to calculate total salary of an employee

basic = float(input("Enter the basic salary: "))

da = 0.10 * basic   # 10% of basic
ta = 0.12 * basic   # 12% of basic
hra = 0.15 * basic  # 15% of basic

total_salary = basic + da + ta + hra

